package br.com.senaisp.bauru.guilherme.licao04.secao03;

public class ExemploString01 {

	public static void main(String[] args) {
		String nome = "Guilherme";
		String nome2 = "Guilherme";
		if (nome == nome2) 
		{
			System.out.println("São iguais");
		}
		else 
		{
			System.out.println("São diferentes");
		}
		
		if (nome.equals(nome == nome2))
		{
			System.out.println("Conteudo iguais");
		}
		else 
		{
			System.out.println("Conteudo diferentes");
		}
		
	}

}
